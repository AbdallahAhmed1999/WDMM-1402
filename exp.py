# integer operations
d = 1 + 4  # int + int = int
print('value of d:', d)
print('type of d:', type(d))

# string operations
# concatenation
s = 'hello ' + 'world ' + '1.0'
print('value of s:', s)
print('type of s:', type(s))

# floats
f1 = 1.3
print('value of f1:', f1)
print('type of f1:', type(f1))
f2 = 10 / 2  # int / int = float
print('value of f2:', f2)
print('type of f2:', type(f2))
