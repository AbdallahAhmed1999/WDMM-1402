def student_average(name, mark1, mark2):
    print('student name', name)
    print('mark1 =', mark1)
    print('mark2 =', mark2)
    average = (mark1 + mark2) / 2
    print('average =', average)
    print('----------------------')


student_average('ahmed', 90, 45)
student_average('ali', 80, 99)
student_average('hasan', 65, 62)
