# function arguments


def func1():  # function with no argument
    print('bla bla ')


def func2(name):  # function with 1 argument
    print('welcome ' + name + ' :(')


def add(n1, n2):  # function with two arguments
    print(n1 + n2)


# call functions
func1()
func2('Ali')
func2('ahmed')
add(3, 4)
add(13, 19)
